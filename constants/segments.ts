export type Segment = {
  id: string;
  road: string;
  name: string; // e.g., "Вакарел → Ихтиман"
  start: { latitude: number; longitude: number };
  finish: { latitude: number; longitude: number };
  limitKmH?: number;
  radiusMeters?: number; // default 30
  path?: { latitude: number; longitude: number }[]; // optional road-following polyline
};

const R = (
  road: string,
  fromName: string,
  toName: string,
  sLat: number,
  sLon: number,
  fLat: number,
  fLon: number
): Segment => ({
  id: `${road}:${fromName}→${toName}`,
  road,
  name: `${fromName} → ${toName}`,
  start: { latitude: sLat, longitude: sLon },
  finish: { latitude: fLat, longitude: fLon },
});

